    private void jugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jugarActionPerformed
        System.out.println("jugar pressed!!!");
        String id = (String) listaK.getSelectedValue();
        String[] i = id.split(" ");
        int ids = Integer.parseInt(i[0]);
        listaK.clearSelection();
        
        vp.setKakuroenjuego(ids);
        jugar.setEnabled(false);
        vp.changeToJuego();
    }//GEN-LAST:event_jugarActionPerformed

    private void volverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_volverActionPerformed
        System.out.println("volver pressed!!!");
        listaK.clearSelection();
        jugar.setEnabled(false);
        vp.changeToNuevaPartida();
    }//GEN-LAST:event_volverActionPerformed

