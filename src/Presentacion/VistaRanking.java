    public VistaRanking(VistaPrincipal vistaPrincipal) {
        initComponents();
        vp = vistaPrincipal;
        content = vp.getRanking();
        String[] users = content.split("\n");
        for (int i = 0; i < users.length; i++)
        {
            tablaRanking.setValueAt(i+1,i,0);
            String[] tokens = users[i].split(",");
            tablaRanking.setValueAt(tokens[0],i,1);
            tablaRanking.setValueAt(tokens[1],i,2);
        }
        
    }
